def crc8(data):
    crc = 0
    for b in data:
        crc = crc8_table[(crc ^ b) & 0xFF]
    return crc & 0xFF

def make_packet(cmd, data):
    d = list(data)
    return bytes([0x51, 0x78, cmd, 0x00, len(d), 0x00] + d + [crc8(d), 0xFF])

PRINT_ROW  = 0xA2
FEED_PAPER = 0xA1

# ── Create test image ─────────────────────────────────────────────────
# 384 px = exact print width of a 4-inch thermal printer
WIDTH = 384
HEIGHT = 600
img = Image.new('L', (WIDTH, HEIGHT), 255)  # grayscale for proper font fill
draw = ImageDraw.Draw(img)

# Font loading: try common system TTF paths, fall back to Pillow built-in
FONT_CANDIDATES = [
    "/usr/share/fonts/truetype/dejavu/DejaVuSans-Bold.ttf",
    "/usr/share/fonts/truetype/liberation/LiberationSans-Bold.ttf",
    "/usr/share/fonts/truetype/ubuntu/Ubuntu-B.ttf",
    "/usr/share/fonts/TTF/DejaVuSans-Bold.ttf",
    "/usr/share/fonts/dejavu/DejaVuSans-Bold.ttf",
]

def load_font(size: int):
    for path in FONT_CANDIDATES:
        if os.path.exists(path):
            try:
                return ImageFont.truetype(path, size)
            except Exception:
                continue
    try:
        return ImageFont.load_default(size=size)   # Pillow >= 10
    except TypeError:
        return ImageFont.load_default()

font_mega = load_font(120)
font_huge = load_font(90)

# Draw text
y_pos = 20
draw.text((10, y_pos), "HELLO", fill=0, font=font_mega)
y_pos += 140
draw.text((10, y_pos), "TANMAY", fill=0, font=font_huge)

# Threshold to clean 1-bit before sending to printer
img = img.point(lambda x: 0 if x < 128 else 255).convert('1')

# Save preview to check before printing
img.save("print_preview.png")
print("Preview saved to: print_preview.png")

# ── Convert to row packets ─────────────────────────────────────────────
def image_to_packets(img):
    # Convert to 'L' (grayscale) so getpixel() returns 0-255 integers.
    # In '1' mode Pillow returns False/True which breaks the px==0 check.
    img_l = img.convert('L')
    packets = []
    for y in range(img_l.height):
        row_bytes = []
        for x in range(0, WIDTH, 8):
            byte = 0
            for bit in range(8):
                px = img_l.getpixel((x + bit, y))
                if px < 128:  # black pixel (0 = black in grayscale)
                    byte |= (1 << (7 - bit))
            row_bytes.append(byte)
        packets.append(make_packet(PRINT_ROW, row_bytes))
    return packets

# ── Send to printer ────────────────────────────────────────────────────
if __name__ == "__main__":
    print(f"Creating {HEIGHT} row packets...")
    packets = image_to_packets(img)

    print("Connecting to printer D9:2C:4C:B4:DA:FF...")
    try:
        sock = socket.socket(socket.AF_BLUETOOTH, socket.SOCK_STREAM, socket.BTPROTO_RFCOMM)
        sock.connect(("D9:2C:4C:B4:DA:FF", 1))
        time.sleep(1)

        print(f"Sending {len(packets)} row packets...")
        for i, pkt in enumerate(packets):
            sock.send(pkt)
            if (i + 1) % 50 == 0:
                print(f"  Sent {i+1}/{len(packets)} rows...")
            time.sleep(0.005)

        print("Feeding paper...")
        sock.send(make_packet(FEED_PAPER, [0x32, 0x00]))
        time.sleep(1)
        sock.close()
        print("✓ Print successful!")
    except Exception as e:
        print(f"✗ Error: {e}")
        print("Make sure printer is powered on and Bluetooth is connected")
